#!/usr/bin/env node
/**
 * Emit KitchenHub recipe import SQL from validated parsed JSON.
 * Run validate-recipe-import.mjs first (must exit 0).
 *
 * Usage:
 *   node emit-recipe-sql.mjs parsed.json --categories "Weeknight,Asian"
 *   node emit-recipe-sql.mjs parsed.json --categories "Weeknight" --new-categories "Asian"
 *   node emit-recipe-sql.mjs parsed.json --categories "Weeknight" --out ../sql/import-single-foo.sql
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { normalizeIngredient } from './item-normalize.mjs';
import { canonicalizeMeasurement } from './measurement-canonical.mjs';
import { escSql, trunc80, trunc255, mergeRecipeLines, slugify } from './sql-utils.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SQL_DIR = path.resolve(__dirname, '../sql');

function usage() {
  console.error(`
emit-recipe-sql.mjs parsed.json --categories "Cat1,Cat2" [options]

Required:
  --categories "A,B"     Existing recipe.recipe_category names to link

Options:
  --new-categories "X,Y"   Also INSERT new category names (ON CONFLICT DO NOTHING)
  --out PATH               Output file (default: sql/import-single-<slug>.sql)
  --no-validate-hint       Omit reminder comment about running validate first

Does not INSERT items or measurements — validate first.
`);
}

function parseArgs(argv) {
  const opts = {
    categories: [],
    newCategories: [],
    out: null,
  };
  const paths = [];
  for (let i = 0; i < argv.length; i += 1) {
    const a = argv[i];
    if (a === '--categories' && argv[i + 1]) {
      opts.categories = argv[++i].split(',').map((s) => s.trim()).filter(Boolean);
    } else if (a === '--new-categories' && argv[i + 1]) {
      opts.newCategories = argv[++i].split(',').map((s) => s.trim()).filter(Boolean);
    } else if (a === '--out' && argv[i + 1]) {
      opts.out = argv[++i];
    } else if (a === '--help' || a === '-h') {
      usage();
      process.exit(0);
    } else if (!a.startsWith('--')) {
      paths.push(a);
    }
  }
  return { opts, paths };
}

function readJson(file) {
  const text =
    file === '-'
      ? fs.readFileSync(0, 'utf8')
      : fs.readFileSync(path.resolve(file), 'utf8');
  return JSON.parse(text);
}

function prepareIngredients(payload) {
  const rows = [];
  for (const line of payload.ingredients || []) {
    let item = line.item;
    let comment = line.comment ?? null;
    if (line.raw) {
      const norm = normalizeIngredient(line.raw);
      item = norm.name;
      if (!comment && norm.comment) comment = norm.comment;
    }
    rows.push({
      qty: Number(line.qty),
      meas: canonicalizeMeasurement(line.measurement),
      name: trunc80(item),
      comment: comment ? trunc255(comment) : null,
    });
  }
  return mergeRecipeLines(rows);
}

/**
 * @param {object} payload
 * @param {{ categories: string[], newCategories: string[] }} opts
 */
export function buildRecipeSql(payload, opts) {
  const name = trunc80(payload.name);
  const servings = Number.parseInt(payload.servings, 10) || 4;
  const instructions = payload.instructions ?? '';
  const ing = prepareIngredients(payload);
  const nameLit = `'${escSql(name)}'`;
  const allCats = [
    ...new Set([...opts.newCategories, ...opts.categories].map((c) => c.trim()).filter(Boolean)),
  ];

  const lines = [];
  lines.push('-- Generated by emit-recipe-sql.mjs');
  lines.push('-- Prerequisites: validate-recipe-import.mjs exited 0');
  lines.push('');
  lines.push('BEGIN;');
  lines.push('');

  if (opts.newCategories.length) {
    lines.push('-- New categories');
    for (const c of opts.newCategories) {
      lines.push(
        `INSERT INTO recipe.recipe_category (name) VALUES ('${escSql(c)}') ON CONFLICT (name) DO NOTHING;`
      );
    }
    lines.push('');
  }

  lines.push(`-- Recipe: ${name}`);
  lines.push('INSERT INTO recipe.recipe (name, servings, instructions) VALUES (');
  lines.push(`    ${nameLit},`);
  lines.push(`    ${servings},`);
  lines.push(`    '${escSql(instructions)}'`);
  lines.push(')');
  lines.push('ON CONFLICT (name) DO UPDATE SET');
  lines.push('    servings = EXCLUDED.servings,');
  lines.push('    instructions = EXCLUDED.instructions;');
  lines.push('');
  lines.push('DELETE FROM recipe.recipe_ingredients');
  lines.push(
    `WHERE recipe_id = (SELECT id FROM recipe.recipe WHERE name = ${nameLit} LIMIT 1);`
  );
  lines.push('');
  lines.push(
    'INSERT INTO recipe.recipe_ingredients (recipe_id, ingredient_id, qty, measurement_id, comment)'
  );
  lines.push('SELECT');
  lines.push(`    (SELECT id FROM recipe.recipe WHERE name = ${nameLit} LIMIT 1),`);
  lines.push('    items.id,');
  lines.push('    recipe_data.qty_val,');
  lines.push('    measurements.id,');
  lines.push("    NULLIF(recipe_data.comment_text, '')");
  lines.push('FROM (');
  lines.push('    VALUES');
  const valueLines = ing.map((r) => {
    const c = r.comment === null ? 'NULL' : `'${escSql(r.comment)}'`;
    return `    (${Number(r.qty)}, '${escSql(r.meas)}', '${escSql(r.name)}', ${c})`;
  });
  lines.push(valueLines.join(',\n'));
  lines.push(
    ') AS recipe_data(qty_val, measurement_name, item_name, comment_text)'
  );
  lines.push('JOIN public.items items ON items.name = recipe_data.item_name');
  lines.push(
    'JOIN common.measurements measurements ON measurements.name = recipe_data.measurement_name;'
  );
  lines.push('');

  if (allCats.length) {
    lines.push('-- Category membership');
    const catList = allCats.map((c) => `'${escSql(c)}'`).join(', ');
    lines.push('INSERT INTO recipe.recipe_category_members (recipe_id, category_id)');
    lines.push('SELECT r.id, c.id');
    lines.push('FROM recipe.recipe r');
    lines.push('CROSS JOIN recipe.recipe_category c');
    lines.push(`WHERE r.name = ${nameLit} AND c.name IN (${catList})`);
    lines.push('ON CONFLICT (recipe_id, category_id) DO NOTHING;');
    lines.push('');
  }

  lines.push('COMMIT;');
  return lines.join('\n');
}

function main() {
  const { opts, paths } = parseArgs(process.argv.slice(2));
  if (paths.length === 0 || opts.categories.length === 0) {
    usage();
    process.exit(1);
  }

  const payload = readJson(paths[0]);
  const sql = buildRecipeSql(payload, {
    categories: opts.categories,
    newCategories: opts.newCategories,
  });

  const outPath =
    opts.out ||
    path.join(SQL_DIR, `import-single-${slugify(payload.name)}.sql`);
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, `${sql}\n`, 'utf8');
  console.log('Wrote', outPath);
}

const isMain =
  process.argv[1] &&
  path.resolve(process.argv[1]) === path.resolve(fileURLToPath(import.meta.url));
if (isMain) {
  main();
}
